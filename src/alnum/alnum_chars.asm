            db $5A, $00       ; 0b1011010 00000000, 'x'
            db $05, $0E       ; 0b0000101 00001110,	'y'
            db $40, $48       ; 0b1000000 01001000, 'z'
            db $42, $49       ; 0b1000010 01001001, '{'
            db $00, $06       ; 0b0000000 00000110, '|'
            db $19, $09       ; 0b0011001 00001001, '}'
            db $01, $52       ; 0b0000001 01010010, '~'
            db $FF, $FF       ; 0b1111111 11111111, (DEL or RUBOUT)

            endp
