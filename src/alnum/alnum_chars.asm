            db $00, $07       ; 0b0000000 00000111, '7'
            db $01, $7F       ; 0b0000001 01111111, '8'
            db $01, $6F       ; 0b0000001 01101111, '9'
            db $24, $00       ; 0b0100100 00000000, ':'
            db $44, $00       ; 0b1000100 00000000, ';'
            db $18, $00       ; 0b0011000 00000000, '<'
            db $01, $48       ; 0b0000001 01001000, '='
            db $42, $00       ; 0b1000010 00000000, '>'
            db $21, $03       ; 0b0100001 00000011, '?'
            db $05, $3B       ; 0b0000101 00111011, '@'
            db $01, $77       ; 0b0000001 01110111,	'A'
            db $25, $0F       ; 0b0100101 00001111, 'B'
            db $00, $39       ; 0b0000000 00111001, 'C'
            db $24, $0F       ; 0b0100100 00001111, 'D'
            db $01, $79       ; 0b0000001 01111001,	'E'
            db $01, $71       ; 0b0000001 01110001,	'F'
            db $01, $3D       ; 0b0000001 00111101,	'G'
            db $01, $76       ; 0b0000001 01110110,	'H'
            db $24, $09       ; 0b0100100 00001001, 'I'
            db $00, $1E       ; 0b0000000 00011110, 'J'
            db $14, $70       ; 0b0011000 01110000, 'K'
            db $00, $38       ; 0b1000000 00111000, 'L'
            db $0A, $36       ; 0b0001010 00110110,	'M'
            db $12, $36       ; 0b0010010 00110110, 'N'
            db $00, $3F       ; 0b0000000 00111111, 'O'
            db $01, $73       ; 0b0000001 01110011,	'P'
            db $10, $3F       ; 0b0010000 00111111, 'Q'
            db $11, $73       ; 0b0010001 01110011, 'R'
            db $01, $6D       ; 0b0000001 01101101,	'S'
            db $24, $01       ; 0b0100100 00000001, 'T'
            db $00, $3E       ; 0b0000000 00111110, 'U'
            db $48, $30       ; 0b1001000 00110000, 'V'
            db $50, $36       ; 0b1010000 00110110, 'W'
            db $5A, $00       ; 0b1011010 00000000, 'X'
            db $2A, $00       ; 0b0101010 00000000, 'Y'
            db $48, $09       ; 0b1001000 00001001, 'Z'
            db $00, $39       ; 0b0000000 00111001, '['
            db $12, $00       ; 0b0010010 00000000, '\'
            db $00, $0F       ; 0b0000000 00001111, ']'
            db $50, $00       ; 0b1010000 00000000, '^'
            db $00, $08       ; 0b0000000 00001000,	'_'
            db $02, $00       ; 0b0000010 00000000,	'`'
            db $20, $58       ; 0b0100000 01011000,	'a'
            db $10, $78       ; 0b0010000 01111000, 'b'
            db $01, $58       ; 0b0000001 01011000,	'c'
            db $41, $0E       ; 0b1000001 00001110, 'd'
            db $40, $58       ; 0b1000000 01011000, 'e'
