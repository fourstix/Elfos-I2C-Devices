;-------------------------------------------------------------------------------
; Table with 14-segment values for ASCII printable characters
;-------------------------------------------------------------------------------


            proc    alnum_chars

                              ;   nmlkjih  gfedcba
            db $00, $00       ; 0b0000000 00000000, ' ' (space)
            db $04, $08       ; 0b0000100 00001000, '!'
            db $04, $02       ; 0b0000100 00000010,  " (double quote)
            db $25, $4E       ; 0b0100101 01001110, '#'
            db $25, $6D       ; 0b0100101 01101101, '$'
            db $5B, $64       ; 0b1011011 01100100, '%'
            db $16, $59       ; 0b0010110 01011001, '&'
            db $04, $00       ; 0b0000100 00000000,  ' (single quote)
            db $18, $40       ; 0b0011000 01000000, '('
            db $43, $00       ; 0b1000011 00000000, ')'
            db $7F, $40       ; 0b1111111 01000000, '*'
            db $25, $40       ; 0b0100101 01000000, '+'
            db $40, $00       ; 0b1000000 00000000, ','
            db $01, $40       ; 0b0000001 01000000, '-'
            db $00, $04       ; 0b0000000 00000100, '.' 
            db $48, $00       ; 0b1001000 00000000, '/'
            db $48, $3F       ; 0b1001000 00111111, '0'
            db $08, $06       ; 0b0001000 00000110, '1'
            db $01, $5B       ; 0b0000001 01011011, '2'
            db $01, $4F       ; 0b0000001 01001111, '3'
            db $01, $66       ; 0b0000001 01100110, '4'
            db $10, $69       ; 0b0010000 01101001, '5'
